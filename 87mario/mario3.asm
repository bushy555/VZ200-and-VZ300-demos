; Ive made this overly more complicated than it should be.
;
; Memory layout:
;   6kb .VZ binary.
;   $7000-$77FF  VZ200 display VRAM (written last, LDIR from work buf)
;   $C000-$E7FF  Work buffer  (rotation output assembled here)
;   $E000-$EFFF  Source buffer (original Mario graphic, never modified)
;
; Will probably only ever work on a real VZ300/Laser310 with the SD Loader!
;
; ============================================================
; R15D_SMOOTH.ASM  -  VZ200 MODE(1) Mario Rotation Demo
; Pure Z80, PASMO assembler, ORG $8000
;
; 512-entry sin/cos tables -> 392 distinct visible angle positions
; per full revolution vs 256 with the previous 256-entry tables.
; That is ~53% more rotational smoothness.
;
; ANGLE is a 16-bit counter (DEFW).
; It increments by 1 each frame and wraps at 512.
; Table lookup: index = ANGLE, each table entry is 1 byte.
; Total table RAM: 512 + 512 = 1024 bytes.
;
;
; Rotation uses fixed-point 8.8 (16-bit signed).
; Scale: 64 = 1.0  (range -64..+64 fits signed byte)
; Per-pixel horizontal step = cos*4 / sin*4  (4 src pixels per byte)
; Per-row vertical step     = -sin / cos
; ROW_TABLE gives $E000 + row*32 to avoid multiply in inner loop.
;
; Spin speed: 1 angle step per frame = 512 frames per full revolution.
; To spin faster, change INC HL to ADD HL,BC with BC=2 (or 4 etc.)
; at the ANGLE increment section near the bottom of MAINLOOP.
; ============================================================

        ORG     $8000

START:

        ; --- Enter MODE(1) ---
        LD      A, 8
        LD      ($6800), A

	di

        ; --- Clear work buffer $C000 ---
        LD      HL, $C000
        LD      DE, $C001
        LD      BC, 2047
        LD      (HL), 0
        LDIR

        ; --- Copy Mario graphic data into $E000 (source, read-only) ---
        LD      HL, MARIO64
        LD      DE, $E000-2048
        LD      BC, 2048; 1152
	ldir

        LD      HL, MARIO64
        LD      DE, $E000
        LD      BC, 2048; 1152
	ldir

        LD      HL, MARIO64
        LD      DE, $E000+2048
        LD      BC, 2048; 1152
	ldir


        ; --- Show original on screen before spinning ---
        LD      HL, $E000
        LD      DE, $7000
        LD      BC, 2048
        LDIR

        ; --- Initialise 16-bit angle to 0 ---
        LD      HL, 0
        LD      (ANGLE), HL

; ==============================================================
; MAIN LOOP
; ==============================================================
MAINLOOP:

;        ; ---- Clear work buffer $C000 ----
;        LD      HL, $C000
;        LD      DE, $C001
;        LD      BC, 2047
;        LD      (HL), 0
;        LDIR
;



        ; ---- Look up cos and sin for current angle ----
        ; ANGLE is 0..511, each entry is 1 byte in 512-entry tables

        LD      HL, (ANGLE)         ; HL = angle index 0..511
        LD      DE, COS_TABLE
        ADD     HL, DE              ; HL = &COS_TABLE[angle]
        LD      A, (HL)             ; A = cos value (signed, scale 64)
        LD      (COS_VAL), A

        LD      HL, (ANGLE)
        LD      DE, SIN_TABLE
        ADD     HL, DE
        LD      A, (HL)             ; A = sin value (signed, scale 64)
        LD      (SIN_VAL), A

        ; ---- Build 16-bit signed cos and sin ----
        ; BC = 16-bit signed cos
        LD      A, (COS_VAL)
        LD      C, A
        RLA
        SBC     A, A
        LD      B, A                ; BC = sign-extended cos

        ; DE = 16-bit signed sin
        LD      A, (SIN_VAL)
        LD      E, A
        RLA
        SBC     A, A
        LD      D, A                ; DE = sign-extended sin

        ; ---- STEP_CX = cos * 4  (horizontal src-x step per dest byte) ----
        LD      H, B
        LD      L, C
        ADD     HL, HL
        ADD     HL, HL
        LD      (STEP_CX), HL

        ; ---- STEP_CY = sin * 4  (horizontal src-y step per dest byte) ----
        LD      H, D
        LD      L, E
        ADD     HL, HL
        ADD     HL, HL
        LD      (STEP_CY), HL

        ; ---- NEG_SIN = -sin  (row-start x advances by -sin per row) ----
        LD      H, D
        LD      L, E
        LD      A, H
        CPL
        LD      H, A
        LD      A, L
        CPL
        LD      L, A
        INC     HL
        LD      (NEG_SIN), HL

        ; ---- Initialise row-start accumulators (8.8 fixed point) ----
        ; Centre of 128x64 image = pixel (64,32) = byte column 16, row 32
        ; ROW_SX = 16 << 8 = $1000  (src x start, in byte units)
        ; ROW_SY = 32 << 8 = $2000  (src y start, in pixel rows)
        LD      HL, $1000
        LD      (ROW_SX), HL
        LD      HL, $2000
        LD      (ROW_SY), HL

        ; ---- Row loop: dest_y 0..63 ----
        XOR     A
        LD      (DEST_Y), A

ROW_LOOP:
        ; Copy row-start to per-row src accumulators
        LD      HL, (ROW_SX)
        LD      (SRC_X8), HL
        LD      HL, (ROW_SY)
        LD      (SRC_Y8), HL

        ; Compute dest pointer: $C000 + dest_y * 32
        LD      A, (DEST_Y)
        LD      L, A
        LD      H, 0
        ADD     HL, HL
        ADD     HL, HL
        ADD     HL, HL
        ADD     HL, HL
        ADD     HL, HL              ; HL = dest_y * 32
        LD      DE, $C000
        ADD     HL, DE
        LD      (DEST_PTR), HL

        ; ---- Column loop: 32 bytes per row ----
        LD      B, 32

COL_LOOP:
        PUSH    BC

        ; -- Clamp src_y to 0..63 --
        LD      HL, (SRC_Y8)
        LD      A, H                ; integer part
        BIT     7, A
        JP      NZ, SY_CLAMP_LO
        CP      64
        JP      C, SY_OK
        LD      A, 63
        JP      SY_STORE
SY_CLAMP_LO:
        XOR     A
SY_STORE:
SY_OK:
        LD      (SRC_Y_INT), A

        ; -- Clamp src_x to 0..31 --
        LD      HL, (SRC_X8)
        LD      A, H
        BIT     7, A
        JP      NZ, SX_CLAMP_LO
        CP      32
        JP      C, SX_OK
        LD      A, 31
        JP      SX_STORE
SX_CLAMP_LO:
        XOR     A
SX_STORE:
SX_OK:
        LD      C, A                ; C = src_x byte offset 0..31

        ; -- Fetch source byte via ROW_TABLE --
        ; ROW_TABLE[src_y] = $E000 + src_y*32  (word)
        LD      A, (SRC_Y_INT)
        LD      L, A
        LD      H, 0
        ADD     HL, HL              ; *2 (word table)
        LD      DE, ROW_TABLE
        ADD     HL, DE              ; HL -> table entry
        LD      A, (HL)             ; low byte of row address
        INC     HL
        LD      H, (HL)             ; high byte of row address
        LD      L, A                ; HL = $E000 + src_y*32
        LD      B, 0
        ADD     HL, BC              ; + src_x byte offset
        LD      A, (HL)             ; A = source pixel byte

        ; -- Write to dest --
        LD      HL, (DEST_PTR)
        LD      (HL), A
        INC     HL
        LD      (DEST_PTR), HL

        ; -- Advance src position horizontally --
        LD      HL, (SRC_X8)
        LD      DE, (STEP_CX)
        ADD     HL, DE
        LD      (SRC_X8), HL

        LD      HL, (SRC_Y8)
        LD      DE, (STEP_CY)
        ADD     HL, DE
        LD      (SRC_Y8), HL

        POP     BC
        DEC     B
        JP      NZ, COL_LOOP

        ; ---- Advance row-start: x += -sin,  y += cos ----
        LD      HL, (ROW_SX)
        LD      DE, (NEG_SIN)
        ADD     HL, DE
        LD      (ROW_SX), HL

        LD      A, (COS_VAL)
        LD      E, A
        RLA
        SBC     A, A
        LD      D, A                ; DE = sign-extended cos
        LD      HL, (ROW_SY)
        ADD     HL, DE
        LD      (ROW_SY), HL

        ; Next row
        LD      A, (DEST_Y)
        INC     A
        LD      (DEST_Y), A
        CP      64
        JP      NZ, ROW_LOOP

;        ; ---- Blit work buffer -> VRAM ----
	LD      HL, $C000
        LD      DE, $7000
   ;     LD      BC, 2048
     ;   LDIR


 	LD 	B, 148          ; 128 iterations of 16 LDIs = 2048 bytes

CPYLOOP:
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	LDI
	DJNZ CPYLOOP



        ; ---- Advance angle (16-bit, wraps at 512) ----
        LD      HL, (ANGLE)
        INC     HL
        INC     HL
        LD      A, H
        CP      2                   ; wrapped past 511? (512 = $0200, H=2)
        JP      NZ, ANGLE_DONE
        LD      HL, 0               ; wrap back to 0
ANGLE_DONE:
        LD      (ANGLE), HL

        JP      MAINLOOP

