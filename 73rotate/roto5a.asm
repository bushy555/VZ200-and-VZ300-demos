; ============================================================
; Roto5 (fixed, fast)  — VZ200/VZ300 MODE(1) rotozoomer
; David Maunder / M365 Copilot  (2026-01-27)
; Rules: ORG $8000, SP=$F000, JP (no JR), A-only absolute (nn),
;        all DB/DW at end, MODE(1) via $6800, 32-byte row stride.
; Notes:
; - Removed EX AF,AF' usage; A is never swapped.
; - Uses EXX only as a scratch lane for table addressing.
; - HL = Ucur (8.8), DE = Vcur (8.8), BC = Ax (8.8)
; - Ay kept in memory (16-bit); Vcur += Ay via 8-bit pair adds.
; - IX = VRAM row destination pointer.
; ============================================================

; ------------------------------
; Constants (EQU does not emit)
; ------------------------------
VRAM_BASE  EQU $7000
IO_LATCH   EQU $6800
STACK_TOP  EQU $F000
buffer equ $a000
; ------------------------------
; Entry / Setup
; ------------------------------
START
 DI
 LD SP,STACK_TOP

 ; MODE(1)
 LD A,8
 LD (IO_LATCH),A

 ; Initial angle and speed
 XOR A
 LD (Angle),A
 LD A,1
 LD (AngSpeed),A

; ------------------------------
; Main frame loop
; ------------------------------
MainLoop
 ; angle = (angle + AngSpeed) & 63
 LD A,(Angle)
 LD H,A
 LD A,(AngSpeed)
 ADD A,H
 AND 63
 LD (Angle),A

 ; Compute step vectors (Ax,Ay,Bx,By) 8.8
 CALL ComputeSteps_Fast

 ; Compute start UV for top-left
 CALL ComputeStartUV

 ; Seed per-row accumulators
 LD A,(U0Lo)
 LD (UrowLo),A
 LD A,(U0Hi)
 LD (UrowHi),A
 LD A,(V0Lo)
 LD (VrowLo),A
 LD A,(V0Hi)
 LD (VrowHi),A

 ; Y counters
 XOR A
 LD (YIndex),A     ; 0..63
 LD A,64
 LD (Y_Rem),A

RowLoop
 LD A,(Y_Rem)
 OR A
 JP Z,FrameDone

 ; -----------------------------------------------
 ; IX = VRAM row pointer = RowTable_Mode1[ YIndex ]
 ; -----------------------------------------------
 LD A,(YIndex)     ; A = row index
 LD E,A
 XOR A
 LD D,A
 LD HL,RowTable_Mode1
 ADD HL,DE
 ADD HL,DE         ; HL = &word
 LD E,(HL)
 INC HL
 LD D,(HL)         ; DE = row address
 PUSH DE
 POP IX            ; IX = row destination

 ; -----------------------------------------------
 ; Per-row working registers
 ; HL = Ucur, DE = Vcur, BC = Ax
 ; -----------------------------------------------
 LD A,(UrowLo)
 LD L,A
 LD A,(UrowHi)
 LD H,A

 LD A,(VrowLo)
 LD E,A
 LD A,(VrowHi)
 LD D,A

 LD A,(AxLo)
 LD C,A
 LD A,(AxHi)
 LD B,A

 ; 32 destination bytes across the row
 LD A,32
 LD (XBytes_Rem),A

ByteLoop
 LD A,(XBytes_Rem)
 OR A
 JP Z,NextRow

 XOR A
 LD (AccByte),A

 ; ===================================================
 ; Build one VRAM byte from 4 samples (2 bits each)
 ; No EX AF,AF' — A remains stable throughout.
 ; Alt set (via EXX) is used only for address math.
 ; Pattern per sample:
 ;   y = D & 63
 ;   base = RowPtrWords[y]      ; 16-bit pointer
 ;   u = H                       ; U_hi
 ;   texel = *(base + u) & 3
 ;   dest = (dest<<2) | texel
 ;   Ucur += Ax ; Vcur += Ay
 ; ===================================================

 ; ---------- Sample #0 ----------
 ; y = V_hi & 63  (A = y)
 LD A,D
 AND 63

 ; alt: HL' = y*2, HL' += RowPtrWords, DE' = [HL']
 EXX
 LD H,0
 LD L,A
 ADD HL,HL
 LD DE,RowPtrWords
 ADD HL,DE
 LD E,(HL)
 INC HL
 LD D,(HL)     ; DE' = base row pointer

 ; u = U_hi (from H), alt: HL' = base + u, A = *(HL')
 EXX
 LD A,H
 EXX
 LD L,A
 LD H,0
 ADD HL,DE
 LD A,(HL)
 EXX

 AND 3
 LD C,A
 LD A,(AccByte)
 ADD A,A
 ADD A,A
 OR C
 LD (AccByte),A

 ; Ucur += Ax (16-bit), Vcur += Ay (8+8)
 ADD HL,BC
 LD A,(AyLo)
 ADD A,E
 LD E,A
 LD A,(AyHi)
 ADC A,D
 LD D,A

 ; ---------- Sample #1 ----------
 LD A,D
 AND 63

 EXX
 LD H,0
 LD L,A
 ADD HL,HL
 LD DE,RowPtrWords
 ADD HL,DE
 LD E,(HL)
 INC HL
 LD D,(HL)
 EXX

 LD A,H
 EXX
 LD L,A
 LD H,0
 ADD HL,DE
 LD A,(HL)
 EXX

 AND 3
 LD C,A
 LD A,(AccByte)
 ADD A,A
 ADD A,A
 OR C
 LD (AccByte),A

 ADD HL,BC
 LD A,(AyLo)
 ADD A,E
 LD E,A
 LD A,(AyHi)
 ADC A,D
 LD D,A

 ; ---------- Sample #2 ----------
 LD A,D
 AND 63

 EXX
 LD H,0
 LD L,A
 ADD HL,HL
 LD DE,RowPtrWords
 ADD HL,DE
 LD E,(HL)
 INC HL
 LD D,(HL)
 EXX

 LD A,H
 EXX
 LD L,A
 LD H,0
 ADD HL,DE
 LD A,(HL)
 EXX

 AND 3
 LD C,A
 LD A,(AccByte)
 ADD A,A
 ADD A,A
 OR C
 LD (AccByte),A

 ADD HL,BC
 LD A,(AyLo)
 ADD A,E
 LD E,A
 LD A,(AyHi)
 ADC A,D
 LD D,A

 ; ---------- Sample #3 ----------
 LD A,D
 AND 63

 EXX
 LD H,0
 LD L,A
 ADD HL,HL
 LD DE,RowPtrWords
 ADD HL,DE
 LD E,(HL)
 INC HL
 LD D,(HL)
 EXX

 LD A,H
 EXX
 LD L,A
 LD H,0
 ADD HL,DE
 LD A,(HL)
 EXX

 AND 3
 LD C,A
 LD A,(AccByte)
 ADD A,A
 ADD A,A
 OR C
 LD (AccByte),A

 ADD HL,BC
 LD A,(AyLo)
 ADD A,E
 LD E,A
 LD A,(AyHi)
 ADC A,D
 LD D,A

 ; ---- write composed byte to VRAM ----
 LD A,(AccByte)
 LD (IX+0),A
 INC IX

 ; next column
 LD A,(XBytes_Rem)
 SUB 1
 LD (XBytes_Rem),A
 JP ByteLoop

NextRow
 ; Urow += Bx
 LD A,(BxLo)
 LD C,A
 LD A,(BxHi)
 LD B,A

 LD A,(UrowLo)
 LD L,A
 LD A,(UrowHi)
 LD H,A
 ADD HL,BC
 LD A,L
 LD (UrowLo),A
 LD A,H
 LD (UrowHi),A

 ; Vrow += By
 LD A,(ByLo)
 LD C,A
 LD A,(ByHi)
 LD B,A
 LD A,(VrowLo)
 ADD A,C
 LD (VrowLo),A
 LD A,(VrowHi)
 ADC A,B
 LD (VrowHi),A

 ; Y-- , YIndex++
 LD A,(Y_Rem)
 SUB 1
 LD (Y_Rem),A
 LD A,(YIndex)
 ADD A,1
 LD (YIndex),A
 JP RowLoop

FrameDone



	ld	hl, $b000
	ld	de, $7000
	ld	bc, 2048
	ldir


        LD      hl, $b000
	ld	de, $b001
        LD      bc, 2048
	ld	(hl), 0
	ldir

;         LD      hl,$b000


 JP MainLoop

; ============================================================
; ComputeSteps_Fast
; Ax = 4*cos(a),  Ay = 4*sin(a),  Bx = -Ay,  By = Ax
; cos(a) = Sin64U[(a+16)&63] - 32,  sin(a) = Sin64U[a] - 32
; Results in 8.8 signed.
; ============================================================
ComputeSteps_Fast
 ; ---- Ax ----
 LD A,(Angle)
 ADD A,16
 AND 63
 LD E,A
 XOR A
 LD D,A
 LD HL,Sin64U
 ADD HL,DE
 LD A,(HL)
 SUB 32
 LD L,A
 LD H,$00
 BIT 7,A
 JP Z,CSF_NoNegAx
 LD H,$FF
CSF_NoNegAx
 ADD HL,HL
 ADD HL,HL
 LD A,L
 LD (AxLo),A
 LD A,H
 LD (AxHi),A

 ; ---- Ay ----
 LD A,(Angle)
 LD E,A
 XOR A
 LD D,A
 LD HL,Sin64U
 ADD HL,DE
 LD A,(HL)
 SUB 32
 LD L,A
 LD H,$00
 BIT 7,A
 JP Z,CSF_NoNegAy
 LD H,$FF
CSF_NoNegAy
 ADD HL,HL
 ADD HL,HL
 LD A,L
 LD (AyLo),A
 LD A,H
 LD (AyHi),A

 ; Bx = -Ay
 LD A,(AyLo)
 XOR $FF
 ADD A,1
 LD (BxLo),A
 LD A,(AyHi)
 XOR $FF
 ADC A,0
 LD (BxHi),A

 ; By = Ax
 LD A,(AxLo)
 LD (ByLo),A
 LD A,(AxHi)
 LD (ByHi),A
 RET

; ============================================================
; ComputeStartUV
; U0 = (32<<8) - Ax*64 - Bx*32
; V0 = (32<<8) - Ay*64 - By*32
; Uses SBC HL,qq with carry cleared each time.
; ============================================================
ComputeStartUV
 ; ---- U0 ----
 LD A,(AxLo)
 LD L,A
 LD A,(AxHi)
 LD H,A
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL        ; HL = Ax*64
 LD B,H
 LD C,L           ; BC = Ax*64

 LD A,(BxLo)
 LD L,A
 LD A,(BxHi)
 LD H,A
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL        ; HL = Bx*32
 LD D,H
 LD E,L           ; DE = Bx*32

 LD HL,$2000
 XOR A
 SBC HL,BC
 XOR A
 SBC HL,DE        ; HL = U0
 LD A,L
 LD (U0Lo),A
 LD A,H
 LD (U0Hi),A

 ; ---- V0 ----
 LD A,(AyLo)
 LD L,A
 LD A,(AyHi)
 LD H,A
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL        ; HL = Ay*64
 LD B,H
 LD C,L           ; BC = Ay*64

 LD A,(ByLo)
 LD L,A
 LD A,(ByHi)
 LD H,A
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL
 ADD HL,HL        ; HL = By*32
 LD D,H
 LD E,L           ; DE = By*32

 LD HL,$2000
 XOR A
 SBC HL,BC
 XOR A
 SBC HL,DE        ; HL = V0
 LD A,L
 LD (V0Lo),A
 LD A,H
 LD (V0Hi),A
 RET

