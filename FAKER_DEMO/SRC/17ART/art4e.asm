	ld	(30779), a
	ld	a, $FF		; SHRG MODE(1) for Latch.	   $EF = COLOUR,0    $FF = COLOUR,1.
	ld	(26624), a
;
;	ld	a, $8		; SHRG MODE(1) for copy of latch.  $EF = COLOUR,0    $FF = COLOUR,1.
;	ld	(30779), a
;	ld	a, $8		; SHRG MODE(1) for Latch.	   $EF = COLOUR,0    $FF = COLOUR,1.
;	ld	(26624), a

	di

	ld	hl, picture			; initial display of pic.
	call	display

;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
;	Scroll LEFT for scroll_width
;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

start2:	ld	b, scroll_width				
l1:	inc	ixl
	ld	hl, picture
;	push	ix
;	pop	de
	ld	d, ixh
	ld	e, ixl
	add	hl, de
	call	display
	djnz	l1	


here: 	jp here

;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
;	Scroll RIGHT for scroll_width
;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
	ld	b, scroll_width			
l2:	dec	ixl
	ld	hl, picture
;	push	ix
;	pop	de
	ld	d, ixh
	ld	e, ixl
	add	hl, de
	call	display
	djnz	l2

	jp	start2



;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
display:
;=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
	push	de
	push	bc

	ld	a, 28		; BANK 0. First 2k of screen.
	out	(32), a

	ex	de, hl
	LD 	hl, 0x6800
vs1c:	BIT 	7,  (hl)
	jr	nZ, vs1c
	LD 	hl, 0x6800
vs1ac:	BIT 	7,  (hl)
	jr	z, vs1ac
	ex	de, hl



	ld	a,  64		; height of 256x192 2k bank 0 screen
	ld	de, screen
disp1: 	ld	bc, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, leftover_wrap	; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp1


	ld	a, 29		; BANK 0. First 2k of screen.
	out	(32), a

	ex	de, hl

	LD 	hl, 0x6800
vs1a:	BIT 	7,  (hl)
	jr	nZ, vs1a
	LD 	hl, 0x6800
vs1aa:	BIT 	7,  (hl)
	jr	z, vs1aa


	ex	de, hl

	ld	a,  64		; height of 256x192 2k bank 1 screen
	ld	de, screen
disp2:	ld	bc, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, leftover_wrap	; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp2

	ld	a, 30		; BANK 0. First 2k of screen.
	out	(32), a

	ex	de, hl
	LD 	hl, 0x6800
vs1b:	BIT 	7,  (hl)
	jr	nZ, vs1b
	LD 	hl, 0x6800
vs1ab:	BIT 	7,  (hl)
	jr	z, vs1ab
	ex	de, hl

	ld	a,  64		; height of 256x192 2k bank 2 screen
	ld	de, screen
disp3: 	ld	bc, 32		; 32x8=256. width of 256x192 screen
	ldir
	ld	bc, leftover_wrap	; add leftover amount for wrap around.
	add	hl, bc
	dec	a
	jr	nz, disp3

	pop	bc
	pop	de
	ret




