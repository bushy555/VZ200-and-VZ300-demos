;scroll_width	equ 	32 ; (1x off-screen screens. 512 pixels wide total)
;scroll_width	equ 	96 ; (3x off-screen screens. 1024 pixels wide total)
;scroll_width   equ	128; (4x off-screen screens. 1280 pixels wide total)
;leftover_wrap	equ 	32 ; (1x off-screen screens. Add leftover amount for wrap around. 512 pixels wide total)
;leftover_wrap	equ 	96 ; (3x off-screen screens. Add leftover amount for wrap around. 1024 pixels wide total)
