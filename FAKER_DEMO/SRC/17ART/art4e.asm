	leftover_wrap	equ 	96;32;128	; (4x off-screen screens) Add leftover amount for wrap around.
	screen		equ	$7000
	ORIGIN		equ	$8000

;1280-256= 1024
;512 -256=  256
;1280-32 = 1248
;512 -32 =  480


