        ; Advance font pointer to next glyph row (4 bytes)
        LD HL,(FONT_PTR)
        LD DE,4
        ADD HL,DE
        LD (FONT_PTR),HL

;        POP BC
        DJNZ ROW_LOOP
SKIP_CHAR:
        LD HL,(MSG_PTR)
        INC HL
        LD (MSG_PTR),HL
        LD A,(CHAR_INDEX)
        INC A
        LD (CHAR_INDEX),A
        JP CHAR_LOOP

END_CHARS:
        ; 5. OR text buffer $A000 onto VRAM $7000
        ;    This composites text pixels on top of colour bars.
        ;    2048 bytes = 8 passes of 256 (B=0 gives 256 DJNZ iterations)
        LD 	HL,$A800
        LD 	DE,$A000
        LD 	C,8
OR_OUTER:LD 	B,0
