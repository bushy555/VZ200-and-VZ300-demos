; Frame structure each loop:
;   1. DRAW_BARS  -> fills VRAM $7000 with 4 colour bars
;   2. Clear off-screen buffer $A000
;   3. Render sinus scroller characters into $A000
;   4. OR $A000 onto VRAM so text appears over bars
;   5. Advance frame counter, delay, repeat
;
; VRAM layout: 64 rows x 32 bytes = 2048 bytes at $7000
; Each byte holds 4 x 2-bit pixels (MODE 1)
;   00=black  01=green  10=red  11=blue
; ============================================================

	ORG $8000

START:  DI
        LD A,8
        LD ($6800),A          ; set MODE(1)
        XOR A
        LD (FRAME),A
        LD (FRAME+1),A
        LD HL,64
        LD (BASE_X),HL

; ============================================================
MAIN_LOOP:

	; Ultra fast 2x 2k buffer CLS.

    LD   (SavedSP),SP

    LD   SP,$A000+2048      ; one past VRAM
    LD   DE,$0      ; blue (10101010)
    LD   B,$0           ; 256 pushes × 4 = 1024
ClearLoop:
    PUSH DE
    PUSH DE
    PUSH DE
    PUSH DE
     DJNZ ClearLoop

    LD   SP,$A000+2048+2048      ; one past VRAM
    LD   DE,$aaaa     ; blue (10101010)
    LD   B,0           ; 256 pushes × 4 = 1024
ClearLoop2:
    PUSH DE
    PUSH DE
    PUSH DE
    PUSH DE
     DJNZ ClearLoop2


    LD   SP,(SavedSP)





        ; 1. Draw colour bars into VRAM
        CALL DRAW_BARS


        ; 3. Scroll BASE_X left; wrap when underflowed ($FFxx)
        LD HL,(BASE_X)
        DEC HL
        LD (BASE_X),HL
        LD A,H
        CP $FF
        JP NZ,OK_X
        LD HL,128
        LD (BASE_X),HL
OK_X:

        ; 4. Render each message character into $A000
        LD HL,MESSAGE
        LD (MSG_PTR),HL
        XOR A
        LD (CHAR_INDEX),A

CHAR_LOOP:
        LD HL,(MSG_PTR)
        LD A,(HL)
        OR A
        JP Z,END_CHARS

        ; ASCII -> font index: font base is '@' (ASCII 64)
        SUB 64
        JP C,SKIP_CHAR
        CP 27
        JP NC,SKIP_CHAR

        ; Font pointer = FONT16 + index*64 (each glyph = 16 rows * 4 bytes)
        LD L,A
        LD H,0
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        LD DE,FONT16
        ADD HL,DE
        LD (FONT_PTR),HL

        ; Sinus Y for this character
        LD A,(CHAR_INDEX)
        ADD A,A
        LD HL,(FRAME)
        ADD A,L
        AND 31
        LD L,A
        LD H,0
        LD DE,SCRL_SIN
        ADD HL,DE
        LD A,(HL)
        LD (BASE_Y),A

        ; X pixel position: BASE_X + char_index*16
        LD HL,(BASE_X)
        LD A,(CHAR_INDEX)
        ADD A,A
        ADD A,A
        ADD A,A
        ADD A,A
        LD E,A
        LD D,0
        ADD HL,DE
        LD (CUR_X),HL
        LD B,16               ; 16 rows per character glyph
ROW_LOOP:
 ;       PUSH BC
        ; Screen row = BASE_Y + (16-B)  i.e. BASE_Y+15 down to BASE_Y+0
        LD A,(BASE_Y)
        ADD A,B
        DEC A
        CP 64
        JP NC,SKIP_ROW        ; skip if off screen
        ; Off-screen buffer row address: $A000 + row*32
        LD L,A
        LD H,0
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL
        ADD HL,HL             ; HL = row*32
        LD A,$A0
        ADD A,H
        LD H,A                ; HL = $A000 + row*32
        ; Add byte column from X (4 pixels per byte, so /4)
        LD DE,(CUR_X)
        SRL D
        RR E
        SRL D
        RR E                  ; DE = CUR_X / 4
        ADD HL,DE
        ; Write 4 bytes of glyph row
        LD DE,(FONT_PTR)
        LD A,(DE)
        LD (HL),A
        INC HL
        INC DE
        LD A,(DE)
        LD (HL),A
        INC HL
        INC DE
        LD A,(DE)
        LD (HL),A
        INC HL
        INC DE
        LD A,(DE)
        LD (HL),A

