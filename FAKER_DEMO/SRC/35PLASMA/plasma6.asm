	jr	NZ,sync2

	LD 	hl,0x6800
sync3:	BIT 	7,(hl)			; fancy wait retrace.
	jr	Z,sync3


	ld	hl, BUFFER
	ld	de, VRAM
	ld	bc, 2048
	ldir



