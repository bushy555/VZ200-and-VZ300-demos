            LD      A,(HL)          ; read VRAM byte
            LD      E,A             ; E = old byte (low address into ROLTAB)
            LD      A,(DE)          ; A = ROLTAB[old_byte] (D=ROLTAB_HI, E=byte)
            LD      (HL),A          ; write transformed byte
            INC     HL              ; advance VRAM pointer
            DEC     BC
            LD      A,B
            OR      C
            JP      NZ,RotLoop

	
	LD 	hl,0x6800
