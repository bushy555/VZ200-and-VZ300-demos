


; SPINNING BEAR
; From an AVI / GIF file.
; Rotates no the spot for 8 turns, then slowly travles right then left, back and fourth whilst still spinning.
;
; 27/Nov/2020.  By Dave.
; 28/Nov/2020.  Added 1-bit BUZZKICK engine with song "Just on Time".
; 20/Feb/2026.  Changed tune to Repeating Itself.
; 25/4/26.      Changed blitting from old to fast routine.

	ORG $8000

	ld 	a,8			; mode (1)
	ld 	($6800),a

	ld	hl, screen		;  BG screen. Can stay, no need to make fast.
	ld	de, $7000
	ld	bc, 2048
	ldir

	ld	ix, fr1
	ld	iy, 0

begin	ld hl,musicdata1

play	di

	ld (drumList+1),hl

	ld a,(hl)
	inc hl
	ld h,(hl)
	ld l,a

	xor a
	ld (songSpeedComp+1),a
	ld (ch1out+1),a
	ld (ch2out+1),a

	ld a,128
	ld (ch1freq+1),a
	ld (ch2freq+1),a
	ld a,1
	ld (ch1delay1+1),a
	ld (ch2delay1+1),a
	ld a,16
	ld (ch1delay2+1),a
	ld (ch2delay2+1),a

	exx
	ld d,a
	ld e,a
	ld b,a
	ld c,a
	push hl
	exx

readRow	ld c,(hl)
	inc hl
	bit 7,c
	jr z,noSpeed
	ld a,(hl)
	inc hl
	or a
	jr nz,noLoop
	ld a,(hl)
	inc hl
	ld h,(hl)
	ld l,a
	jr readRow
noLoop	ld (songSpeed+1),a
noSpeed	bit 6,c
	jr z,noSustain1
	ld a,(hl)
	inc hl
	exx
	ld d,a
	ld e,a
	exx

noSustain1

	bit 5,c
	jr z,noSustain2

	ld a,(hl)
	inc hl
	exx
	ld b,a
	ld c,a
	exx

noSustain2

	bit 4,c
	jr z,noNote1

	ld a,(hl)
	ld d,a
	inc hl
	or a
	jr z,$+4
;	ld a,$18
	ld	a, 32
	ld (ch1out+1),a
	jr z,noNote1

	ld a,d
	ld (ch1freq+1),a
	srl a
	srl a
	ld (ch1delay2+1),a
	ld a,1
	ld (ch1delay1+1),a

	exx
	ld e,d
	exx

noNote1	bit 3,c
	jr z,noNote2

	ld a,(hl)
	ld e,a
	inc hl
	or a
	jr z,$+4
;	ld a,$18
	ld	a, 32

	ld (ch2out+1),a
	jr z,noNote2

	ld a,e
	ld (ch2freq+1),a
	srl a
	srl a
	srl a
	ld (ch2delay2+1),a
	ld a,1
	ld (ch2delay1+1),a

	exx
	ld c,b
	exx

noNote2	ld a,c
	and 7
	jr z,noDrum

playDrum

	push hl

	add a,a
	add a,a
	ld c,a
	ld b,0
drumList:
	ld hl,0
	add hl,bc

	ld a,(hl)				;length in 256-sample blocks
	ld b,a
	inc hl
	inc hl

	add a,a
	add a,a
	ld (songSpeedComp+1),a

	ld a,(hl)
	inc hl
	ld h,(hl)				;sample data
	ld l,a

	ld a,1
	ld (mask+1),a

	ld c,0
loop0
	ld a,(hl)				;7
mask:
	and 0					;7
	sub 1					;7
	sbc a,a					;4
	and 32
	or 8
	ld	(26624), a
	ld a,(mask+1)			;13
	rlc a					;8
	ld (mask+1),a			;13
	jr nc,$+3				;7/12
	inc hl					;6

	jr $+2					;12
	jr $+2					;12
	jr $+2					;12
	jr $+2					;12
	nop						;4
	nop						;4
	ld a,0					;7

	dec c					;4
	jr nz,loop0			;7/12=168t
	djnz loop0

	pop hl

noDrum
songSpeed:
	ld a,0
	ld b,a
songSpeedComp:
	sub 0
	jr nc,$+3
	xor a
	ld c,a

	ld a,(songSpeedComp+1)
	sub b
	jr nc,$+3
	xor a
	ld (songSpeedComp+1),a

	ld a,c
	or a
	jp z,readRow

	ld c,a
	ld b,64

soundLoop

	ld a,3				;7
	dec a				;4
	jr nz,$-1			;7/12=50t
	jr $+2				;12

	dec d				;4
	jp nz,ch2			;10

ch1freq:ld d,0				;7

ch1delay1:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12

ch1out:	ld a,0				;7
	and 32
	or 8
	ld	(26624), a

ch1delay2:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12
	and 32
	or 8
	ld	(26624), a
ch2	ld a,3				;7
	dec a				;4
	jr nz,$-1			;7/12=50t
	jr $+2				;12

	dec e				;4
	jp nz,loop			;10

ch2freq:ld e,0				;7

ch2delay1:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12

ch2out:	ld a,0				;7
	and 32
	or 8
	ld	(26624), a


ch2delay2:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12
	and 32
	or 8
	ld	(26624), a

loop	dec b				;4
	jr nz,soundLoop		;7/12=168t
	ld b,64

envelopeDown

	exx
	dec e
	jp nz,noEnv1
	ld e,d

	ld hl,ch1delay2+1
	dec (hl)
	jr z,$+5
	ld hl,ch1delay1+1
	inc (hl)

noEnv1	dec c
	jp nz,noEnv2
	ld c,b

	ld hl,ch2delay2+1
	dec (hl)
	jr z,$+5
	ld hl,ch2delay1+1
	inc (hl)

noEnv2	exx
	dec c
	jp nz,soundLoop

	push	hl		; IX = Global frame pointer.
	push	de		; IY = Global frame counter.

	ld	a, iyl		
	cp	24
	jp	m, here4	; 'a>24'.  IF A>24 then A=1...
	ld	ix, fr1 
		
	ld	hl, fr1 
	ld	a, 1
	jr	here6

here4:	inc	a		; inc frame counter.
	ld	bc, 896		; inc frame pointer to next frame.
	add	ix , bc		; 896 = 56x64pixels. (14x64 bytes)

	push	ix
	pop	hl		; HL = correct source frame.

	

here6:
	ld	iyl, a
SHOW:	ld	de, $7000+9
	ld	c, 0
	ld	b, 32			; Y = 64 rows, but unroll twice so 64-->32

LOOP2:	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
				; 14 bytes or 56 pixels at a time.
	inc	de			; each 'inc de' = increasing 4x pixels.
	inc	de			;  ...increasing the destination on video buffer to get into right position
	inc	de			;     for next lot of LDIR / for next line of 1-64 Y-axis of frame.
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
				; 14 bytes or 56 pixels at a time.
	inc	de			; each 'inc de' = increasing 4x pixels.
	inc	de			;  ...increasing the destination on video buffer to get into right position
	inc	de			;     for next lot of LDIR / for next line of 1-64 Y-axis of frame.
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	djnz	LOOP2			; B=0 yet? No? then, we do loopy.

	pop	de
	pop	hl

	jp	readRow






