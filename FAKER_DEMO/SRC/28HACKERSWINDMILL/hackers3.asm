; ================================================================
; VZ200 / VZ300 - MODE(1) Chessboard Warper (Yellow/Red) with SINE phase
; Animate only rows Y=32..63. Rows 0..31 are left untouched
; so you can place a static graphic in the top half of the buffer.
; - 128x64, 2 bpp (MODE(1))
; - Single per-row horizontal phase: s = SINE[(Y + Time) & 127], 0..127
; - Tiles are 8x8; color = ((x>>3) XOR (y>>3)) ? Red : Yellow
; - Build each row into RowBuf[32], then LDIR to back-buffer row
; - Back buffer at $A000..$A7FF, then full-frame LDIR -> VRAM $7000
; Strict PASMO rules: ORG $8000, SP $F000, JP only, A-only (nn), all data at end
; ================================================================

        ORG     $8000

Start:
	di

        LD      SP,$B000

; Enter MODE(1)
        LD      A,8
        LD      ($6800),A



	call	intro



	ld	hl, hackers
	ld	de, $a000
	ld	bc, 2048 - 14*32
	ldir

; --------------------------
; Main animation loop
; --------------------------
MainLoop:
        XOR     A
        LD      (YIdx),A

RowLoop:
        ; ---- Load Y
        LD      A,(YIdx)
        LD      B,A                 ; B = Y

        ; ---- SKIP TOP HALF (Y=0..31) ----
        ; If Y < 32, skip building/copying this row.
        ; This preserves rows 0..31 in $A000 for your static graphic.
        CP      40;32
        JP      C,SkipRowBuild

        ; ---- Y parity for 8x8 tiles: (Y>>3)&1 -> (YcTmp)
        LD      A,B
        SRL     A
        SRL     A
        SRL     A
        AND     1
        LD      (YcTmp),A

        ; ---- Compute s = SINE[(Y + Time) & 127] -> SVal
        LD      A,(Time)
        ADD     A,B
        AND     127                 ; A = i (0..127)

        ; Quarter-wave sine via 32-byte table with symmetry:
        ; q = i>>5 (0..3), j = i & 31
        LD      H,A                 ; keep i in H
        LD      L,A

        LD      A,H
        SRL     A
        SRL     A
        SRL     A
        SRL     A
        SRL     A
        AND     3
        LD      C,A                 ; C = q

        LD      A,L
        AND     31
        LD      B,A                 ; B = j

        ; idx = j or (31-j) depending on quadrant (q==1 or 3 -> reverse)
        LD      E,B                 ; default idx = j
        LD      A,C
        CP      1
        JP      Z,QS_UseRev
        CP      3
        JP      Z,QS_UseRev
        JP      QS_HaveIdx
QS_UseRev:
        LD      A,31
        SUB     B
        LD      E,A
QS_HaveIdx:
        ; val = QuarterSine[idx]
        LD      HL,QuarterSine
        LD      B,0
        LD      C,E
        ADD     HL,BC
        LD      A,(HL)              ; val 0..63
        LD      D,A                 ; D = val

        ; s = 64 +/- val, + for q in {0,1} else -
        LD      A,C
        AND     2
        JP      Z,QS_Add
        LD      A,64
        SUB     D
        JP      QS_StoreS
QS_Add:
        LD      A,64
        ADD     A,D
QS_StoreS:
        AND     127
        LD      (SVal),A

        ; ---- Build RowBuf[32] for this Y (animated bottom half)
        LD      DE,RowBuf           ; DE = RowBuf write pointer
        XOR     A
        LD      (XIdx),A
RowXLoop:
        ; baseX = (XIdx << 2)
        LD      A,(XIdx)
        SLA     A
        SLA     A
        LD      L,A                 ; L = baseX

        ; Xsum = (baseX + s) & 127
        LD      A,(SVal)
        ADD     A,L
        AND     127
        LD      L,A                 ; L = Xsum

        ; r = Xsum & 7
        LD      A,L
        AND     7
        LD      C,A                 ; C = r (0..7)

        ; parity0 = ((Xsum>>3) XOR Yc) & 1
        LD      H,L
        SRL     H
        SRL     H
        SRL     H
        LD      A,(YcTmp)
        XOR     H
        AND     1
        JP      Z,UsePatternY
UsePatternR:
        LD      HL,BytePatternR
        JP      HaveBase
UsePatternY:
        LD      HL,BytePatternY
HaveBase:
        LD      B,0
        ADD     HL,BC               ; HL -> pattern byte
        LD      A,(HL)

        ; Store to RowBuf
        LD      (DE),A
        INC     DE

        ; XIdx++
        LD      A,(XIdx)
        INC     A
        LD      (XIdx),A
        CP      32
        JP      NZ,RowXLoop

        ; ---- Copy RowBuf to back-buffer row Y (at $A000 + 32*Y)
        LD      HL,RowBuf

        LD      A,(YIdx)
        LD      H,0
        LD      L,A
        ADD     HL,HL               ; 2*Y
        LD      BC,RowBaseTable
        ADD     HL,BC               ; &RowBaseTable[Y]
        LD      E,(HL)
        INC     HL
        LD      D,(HL)              ; DE = back-buffer row address

        LD      HL,RowBuf
        LD      BC,32
        LDIR

SkipRowBuild:
        ; ---- Y++
        LD      A,(YIdx)
        INC     A
        LD      (YIdx),A
        CP      64
        JP      NZ,RowLoop

        ; ---- Advance time
        LD      A,(Time)
        INC     A
        LD      (Time),A

        ; ---- Present frame: copy whole 2KB back buffer -> VRAM
        ; (Keeps your static top half and animated bottom half.)
        LD      HL,$A000
        LD      DE,$7000
        LD      BC,2048
        LDIR
        JP      MainLoop


intro:	LD	A, 8			; Set Mode(1)
	LD	($6800), A
	LD	HL, final1		; Select source of graphic
	LD	DE, $7000		; Select destination, being the video RAM at $7000
	LD	BC, 2048		; Set the size of the screen (2k)
	LDIR				; Copy it all over from source to destination

	call	delay

	LD	HL, final2		; Select source of graphic
	LD	DE, $7000		; Select destination, being the video RAM at $7000
	LD	BC, 2048		; Set the size of the screen (2k)
	LDIR				; Copy it all over from source to destination

	call	delay

	ret


delay:	ld	b, 18
SD_01   LD      D,200
SD_O:	LD      E,255
SD_I:   DEC     E
	JP      NZ,SD_I
        DEC     D
        JP      NZ,SD_O
	djnz	SD_01
	ret



