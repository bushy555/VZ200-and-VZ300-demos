


; SPINNING BEAR  --- From a GIF file found in 2014.
; Rotates on the spot with 1-bit audio playing.
;
; 27/Nov/2020.  By Dave.
; 28/Nov/2020.  Added 1-bit BUZZKICK engine with song "Just on Time".
; 29/Nov/2020.  Some attempt at removing screen noise. Affects music too much, and have given up.
;	        As at 29/Nov/20, tested and works on a real VZ300.


	ORG $8000
vid_buffer	equ	$e800	

	ld 	a,8			; mode (1)
	ld 	($6800),a

	ld	hl, vid_buffer		;  CLS video buffer
	ld	a, 0
	ld	(hl), a
	ld	de, vid_buffer + 1
	ld	bc, 2048
	ldir

	ld	hl, screen1 		; DO "PANDA". 12x64 image.
scrn01: ld	de, vid_buffer + 2
	call	do_init_scrn

	ld	hl, screen2		; DO "007". 12x64 image.
scrn02: ld	de, vid_buffer + 32 - 5
	call	do_init_scrn





	ld	ix, fr1			; initialise frame pointer.
	ld	iy, 1			; initialise frame counter	

begin	ld hl,musicdata1
play	di
	ld (drumList+1),hl

	ld a,(hl)
	inc hl
	ld h,(hl)
	ld l,a

	xor a
	ld (songSpeedComp+1),a
	ld (ch1out+1),a
	ld (ch2out+1),a

	ld a,128
	ld (ch1freq+1),a
	ld (ch2freq+1),a
	ld a,1
	ld (ch1delay1+1),a
	ld (ch2delay1+1),a
	ld a,16
	ld (ch1delay2+1),a
	ld (ch2delay2+1),a

	exx
	ld d,a
	ld e,a
	ld b,a
	ld c,a
	push hl
	exx

readRow	ld c,(hl)
	inc hl
	bit 7,c
	jr z,noSpeed
	ld a,(hl)
	inc hl
	or a
	jr nz,noLoop
	ld a,(hl)
	inc hl
	ld h,(hl)
	ld l,a
	jr readRow
noLoop	ld (songSpeed+1),a
noSpeed	bit 6,c
	jr z,noSustain1
	ld a,(hl)
	inc hl
	exx
	ld d,a
	ld e,a
	exx

noSustain1

	bit 5,c
	jr z,noSustain2

	ld a,(hl)
	inc hl
	exx
	ld b,a
	ld c,a
	exx

noSustain2

	bit 4,c
	jr z,noNote1

	ld a,(hl)
	ld d,a
	inc hl
	or a
	jr z,$+4
;	ld a,$18
	ld	a, 32
	ld (ch1out+1),a
	jr z,noNote1

	ld a,d
	ld (ch1freq+1),a
	srl a
	srl a
	ld (ch1delay2+1),a
	ld a,1
	ld (ch1delay1+1),a

	exx
	ld e,d
	exx

noNote1	bit 3,c
	jr z,noNote2

	ld a,(hl)
	ld e,a
	inc hl
	or a
	jr z,$+4
;	ld a,$18
	ld	a, 32

	ld (ch2out+1),a
	jr z,noNote2

	ld a,e
	ld (ch2freq+1),a
	srl a
	srl a
	srl a
	ld (ch2delay2+1),a
	ld a,1
	ld (ch2delay1+1),a

	exx
	ld c,b
	exx

noNote2	ld a,c
	and 7
	jr z,noDrum

playDrum

	push hl

	add a,a
	add a,a
	ld c,a
	ld b,0
drumList:
	ld hl,0
	add hl,bc

	ld a,(hl)				;length in 256-sample blocks
	ld b,a
	inc hl
	inc hl

	add a,a
	add a,a
	ld (songSpeedComp+1),a

	ld a,(hl)
	inc hl
	ld h,(hl)				;sample data
	ld l,a

	ld a,1
	ld (mask+1),a

	ld c,0
loop0
	ld a,(hl)				;7
mask:
	and 0					;7
	sub 1					;7
	sbc a,a					;4
	and 32
	or 8
	ld	(26624), a
	ld a,(mask+1)			;13
	rlc a					;8
	ld (mask+1),a			;13
	jr nc,$+3				;7/12
	inc hl					;6

	jr $+2					;12
	jr $+2					;12
	jr $+2					;12
	jr $+2					;12
	nop						;4
	nop						;4
	ld a,0					;7

	dec c					;4
	jr nz,loop0			;7/12=168t
	djnz loop0

	pop hl

noDrum
songSpeed:
	ld a,0
	ld b,a
songSpeedComp:
	sub 0
	jr nc,$+3
	xor a
	ld c,a

	ld a,(songSpeedComp+1)
	sub b
	jr nc,$+3
	xor a
	ld (songSpeedComp+1),a

	ld a,c
	or a
	jp z,readRow

	ld c,a
	ld b,64

soundLoop

	ld a,3				;7
	dec a				;4
	jr nz,$-1			;7/12=50t
	jr $+2				;12

	dec d				;4
	jp nz,ch2			;10

ch1freq:ld d,0				;7

ch1delay1:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12

ch1out:	ld a,0				;7
	and 32
	or 8
	ld	(26624), a

ch1delay2:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12
	and 32
	or 8
	ld	(26624), a
ch2	ld a,3				;7
	dec a				;4
	jr nz,$-1			;7/12=50t
	jr $+2				;12

	dec e				;4
	jp nz,loop			;10

ch2freq:ld e,0				;7

ch2delay1:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12

ch2out:	ld a,0				;7
	and 32
	or 8
	ld	(26624), a


ch2delay2:
	ld a,0				;7
	dec a				;4
	jr nz,$-1			;7/12
	and 32
	or 8
	ld	(26624), a

loop	dec b				;4
	jr nz,soundLoop		;7/12=168t
	ld b,64

envelopeDown

	exx
	dec e
	jp nz,noEnv1
	ld e,d

	ld hl,ch1delay2+1
	dec (hl)
	jr z,$+5
	ld hl,ch1delay1+1
	inc (hl)

noEnv1	dec c
	jp nz,noEnv2
	ld c,b

	ld hl,ch2delay2+1
	dec (hl)
	jr z,$+5
	ld hl,ch2delay1+1
	inc (hl)

noEnv2	exx
	dec c
	jp nz,soundLoop

; END OF MUSIC ENGINE
; ------------------------------------------------------------------------
; Start of frame counting . slow, and needs to be re-written to be way faster.


	push	hl		; IX = Global frame pointer.
	push	de		; IY = Global frame counter.

	ld	a, iyl		
	cp	24
	jp	m, here4	; 'a>24'.  IF A>24 then A=1...
	ld	ix, fr1 	; frame counter is at #25, so we reset back to #1.
	ld	hl, fr1 
	ld	iyl, 1		; reset frame counter.
	jp	here6


				; frame counter is 1 to 23...  so we:
here4:	inc	a		; inc frame counter.
	ld	iyl, a
	ld	bc, 896		; inc frame pointer to next frame.
	add	ix , bc		; 896 = 56x64pixels. (14x64 bytes)
	push	ix
	pop	hl		; HL = correct source frame.

	
; -------------------------------------
; draw whichever 1-24 frames to vid_buffer.


here6:
SHOW:	ld	de, vid_buffer +9
	ld	c, 0
	ld	b, 64			; Y = 64 rows

LOOP2:	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
	ldi
				; 14 bytes or 56 pixels at a time.
	inc	de		; each 'inc de' = increasing 4x pixels.
	inc	de		;  ...increasing the destination on video buffer to get into right position
	inc	de		;     for next lot of LDIR / for next line of 1-64 Y-axis of frame.
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	inc	de
	djnz	LOOP2			; B=0 yet? No? then, we do loopy.



	LD 	hl,0x6800
sync1:	BIT 	7,(hl)			; fancy wait retrace.
	jr	NZ,sync1

	ld	hl, vid_buffer		;  Blit 1k from vid buffer to screen. 
	ld	de, $7000
	ld	bc, 2048
	ldir

;	LD 	hl,0x6800
;sync2:	BIT 	7,(hl)			; fancy wait retrace.
;	jr	NZ,sync2
;
;	ld	hl, vid_buffer + 1024	; blit 1k from vid buffer to screen.
;	ld	de, $7000+1024
;	ld	bc, 1024
;	ldir

	pop	de
	pop	hl

	jp	readRow



; -----------------------------------------------------
; This shoved into a routine to save a few bytes. 
; Called twice to draw PANDA and 007 into buffer screen
; 

do_init_scrn:
	ld	c, 0
	ld	b, 64			
LOOPa:	ldi
	ldi
	ldi
	ld	ix, 29
	add	ix, de
	push	ix
	pop	de
	djnz	LOOPa			
	ret



